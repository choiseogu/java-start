package varialbe;

//변수타입
public class Var7 {
    public static void main(String[] args) {
        int a = 100;
        double b = 10.5; //double은 실수 자료형
        boolean c = true; //true, false만 저장가능
        char d = 'A'; //문자 하나만 들어갈 수 있는 char 자료형
        String e = "hello world"; // 문자열이 들어갈 수 있는 string 자료형

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
